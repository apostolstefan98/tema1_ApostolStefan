    private void mbDeschidereActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mbDeschidereActionPerformed
        if (fc2.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) { //deschiderea tabului de deschidere
            File f = fc2.getSelectedFile();   //selecatea fisierului care trebuie deschis
            try {
                FileInputStream fis = new FileInputStream(f);
                ObjectInputStream ois = new ObjectInputStream(fis);   //deschiderea 
                a.Contacte = (List) ois.readObject();  //citirea agendei
                fis.close();
                ois.close(); //inchiderea
            } catch (IOException iOException) {
                JOptionPane.showMessageDialog(this, "Eroare la citire");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            l.setModel(model);  //setarea modelului in caz ca nu exista contacte.
            actualizeazaModel(a.Contacte);  //actualizarea modelului.
        }
    }//GEN-LAST:event_mbDeschidereActionPerformed

