    private void mbSalvareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mbSalvareActionPerformed
        if (fc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {  //deschiderea tabului de salvare
            File f = fc.getSelectedFile();   //selectarea fisierului
            Timer t = new Timer();   //setarea timer ului de autosalvare
            TimerTask task = new TimerTask() {
                @Override
                public void run() {
                    try {
                        FileOutputStream fos = new FileOutputStream(f);    //salvarea
                        ObjectOutputStream oos = new ObjectOutputStream(fos);  //salvarea
                        oos.writeObject(a.Contacte);   //scrierea agendei
                        oos.close();   //inchiderea fiserului de iesire
                        fos.close();
                    } catch (IOException ex) {
                        Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            };
            t.schedule(task, 0, 60000);   //porinrea timer ului de salvare automata la 1 minut
        }
    }//GEN-LAST:event_mbSalvareActionPerformed

